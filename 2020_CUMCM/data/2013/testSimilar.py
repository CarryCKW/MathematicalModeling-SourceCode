from difflib import SequenceMatcher


def Similar(a, b):
    return SequenceMatcher(None, a, b).ratio()


str1 = '0001110001110000000001110000111000111'
str2 = '0001111111110000000001110000111111111'
str3 = '0001111111110000000001110000111111100'
str4 = '1000111111111000000000111000011111110'
print(Similar(str3, str4))